package sessiondemo;

public class MemDTO {
	private int num;
	private String name;
	private String fid;
	private String fpass;
	
	public MemDTO() {
		
	}

	public int getNum() {
		return num;
	}

	public void setNum(int num) {
		this.num = num;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getFid() {
		return fid;
	}

	public void setFid(String fid) {
		this.fid = fid;
	}

	public String getFpass() {
		return fpass;
	}

	public void setFpass(String fpass) {
		this.fpass = fpass;
	}
	
	
}
